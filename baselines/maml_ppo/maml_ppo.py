def constfn(val):
    def f(_):
        return val
    return f


def learn(*, policy, env, ntasks, nsteps, total_timesteps, ent_coef, lr, inner_lr,
            vf_coef=0.5,  max_grad_norm=0.5, gamma=0.99, lam=0.95,
            log_interval=10, nminibatches=4, noptepochs=4, cliprange=0.2,
            save_interval=0):
    """ Run training.
    Args:
        policy: the policy object
        env: the environment object which encapsulates multiple tasks
        ntasks: the meta-batch size
        nsteps: the number of inner timesteps per task (right now also the number of outer steps per task)
        *If more than one env in the vec env, then this is per env in the vec env!*
        total_timesteps: the total number of steps to run training for
        ent_coef: entropy coefficient
        lr: outer step size
        inner_lr: inner step size

    """
    if isinstance(lr, float): lr = constfn(lr)
    else: assert callable(lr)
    if isinstance(cliprange, float): cliprange = constfn(cliprange)
    else: assert callable(cliprange)
    total_timesteps = int(total_timesteps)

    nenvs = env.num_envs
    ob_space = env.observation_space
    ac_space = env.action_space
    nbatch = nenvs * nsteps
    nbatch_train = nbatch // nminibatches

    make_model = lambda : Model(policy=policy, ob_space=ob_space, ac_space=ac_space, ntasks=ntasks, nbatch_act=nenvs, nbatch_train=nbatch_train,
                    nsteps=nsteps, ent_coef=ent_coef, vf_coef=vf_coef,
                    max_grad_norm=max_grad_norm)
    if save_interval and logger.get_dir():
        import cloudpickle
        with open(osp.join(logger.get_dir(), 'make_model.pkl'), 'wb') as fh:
            fh.write(cloudpickle.dumps(make_model))
    model = make_model()
    runners = [SingleTaskRunner.remote(env=env, policy=model.act_policy, nsteps=nsteps, gamma=gamma, lam=lam) for _ in range(ntasks)]

    epinfobuf = deque(maxlen=100)
    tfirststart = time.time()

    nupdates = total_timesteps//nbatch
    for update in range(1, nupdates+1):
        assert nbatch % nminibatches == 0
        nbatch_train = nbatch // nminibatches
        tstart = time.time()
        frac = 1.0 - (update - 1.0) / nupdates
        lrnow = lr(frac)
        cliprangenow = cliprange(frac)

        for runner in runners:
            runner.reset_task.remote()

        # gather pre-update data
        preupdate_weights = model.get_preupdate_weights()
